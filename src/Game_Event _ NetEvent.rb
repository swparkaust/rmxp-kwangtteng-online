# 　이벤트를 취급하는 클래스입니다.조건 판정에 의한 이벤트 페이지 변환이나, 병렬처리
# 이벤트 실행등의 기능을 가지고 있어 Game_Map 클래스의 내부에서 사용됩니다.
#==============================================================================

class Game_Event
  #--------------------------------------------------------------------------
  # ● 공개 인스턴스 변수
  #--------------------------------------------------------------------------
  attr_accessor :erased
  #--------------------------------------------------------------------------
  # ● 넷 이벤트 판정
  #--------------------------------------------------------------------------
  def netevent?
    if comment_include(self, "/넷이벤트", false) != nil
      return true
    else
      return false
    end
  end
  #--------------------------------------------------------------------------
  # ● 일시 소거
  #--------------------------------------------------------------------------
  def erase
    if self.netevent?
      send sprintf("netevent,1,%d,%d", $game_map.map_id, @id)
    else
      @erased = true
    end
  end
end
